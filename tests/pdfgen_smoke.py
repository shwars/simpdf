import sys
from pathlib import Path

REPO_ROOT = Path(__file__).resolve().parent.parent
if str(REPO_ROOT) not in sys.path:
    sys.path.insert(0, str(REPO_ROOT))

import pdfgen


def assert_pdf_binary(case_name, data):
    if not isinstance(data, (bytes, bytearray)):
        raise AssertionError(f"{case_name}: expected bytes, got {type(data)}")
    if len(data) <= 100:
        raise AssertionError(f"{case_name}: PDF too small ({len(data)} bytes)")
    if bytes(data[:4]) != b"%PDF":
        raise AssertionError(f"{case_name}: missing %PDF header")


def run():
    fonts_dir = (REPO_ROOT / ".." / "sysbucket" / "fonts").resolve()
    if not fonts_dir.exists():
        raise RuntimeError(f"Fonts directory not found: {fonts_dir}")

    pdfgen.set_font_files(fonts_dir)

    examples = [
        ("plain", "Hello from PDF smoke test"),
        ("cyrillic", "Привет, это тест PDF с кириллицей."),
        ("markdown", "# Header\nThis is **bold** and this is *italic*."),
        (
            "table",
            "| Name | Value |\n|------|------|\n| One | 1 |\n| Two | 2 |",
        ),
        ("long","""
**Таблица: размеры папулы при пробе Манту и их интерпретация**

| Размер папулы (мм) | Интерпретация | Пояснения |
| --- | --- | --- |
| **0–1** | Отрицательная реакция | Признаков сенсибилизации к туберкулину нет. Вероятно, инфицирования *M. tuberculosis* нет (но полностью не исключается). |
| **2–4** | Сомнительная реакция | Возможна как норма, так и слабая реакция. Требуется наблюдение, возможно — дообследование. |
| **5–9** | Слабоположительная реакция | Может указывать на инфицирование либо на поствакцинальный иммунитет (после БЦЖ). Необходимо оценить факторы риска и динамику реакций. |
| **10–14** | Умеренно положительная реакция | Высокая вероятность инфицирования. Показано обследование у фтизиатра (флюорография/рентген, анализы). |
| **15–16** | Выраженно положительная реакция | Высокая вероятность активного процесса или недавнего инфицирования. Обязательно обследование у специалиста. |
| **17 и более** | Гиперергическая реакция | Очень высокая вероятность инфицирования или активного туберкулёза. Требуется срочное дообследование. |
| **Любой размер + везикуло‑некротические изменения, лимфангит** | Гиперергическая реакция | Признаки выраженной местной реакции — высокий риск активного процесса. Срочно обратиться к фтизиатру. |

**Важные уточнения:**

1. **Измерение.** Папулу измеряют через 72 часа после постановки пробы (в мм), оценивая только уплотнённый участок (не покраснение).
2. **Контекст.** Интерпретация зависит от:
   - возраста и статуса вакцинации БЦЖ;
   - наличия контактов с больными туберкулёзом;
   - сопутствующих заболеваний (особенно иммунодефицитов);
   - региона проживания (эндемичность).
3. **Динамика.** Нарастание размера папулы от пробы к пробе («вираж») — тревожный признак, требующий обследования.
4. **Ограничения пробы Манту:**
   - не подтверждает диагноз «туберкулёз»;
   - может давать ложноположительные/ложноотрицательные результаты (из‑за аллергии, техники постановки, иммунного статуса и др.).
5. **Дальнейшие шаги.** При положительной/гиперергической реакции назначают:
   - флюорографию/рентген грудной клетки;
   - анализы крови и мокроты;
   - альтернативные тесты (Диаскинтест, квантифероновый тест, T‑SPOT.TB).

**Вывод:** проба Манту — скрининговый инструмент. Окончательное заключение делает врач на основании комплекса данных.
""")
    ]

    failures = []
    n=0
    for case_name, text in examples:
        try:
            pdf_data = pdfgen.render_text_to_pdf_bytes(text)
            assert_pdf_binary(case_name, pdf_data)
            with open(f"pdf_{n}.pdf","wb") as f:
              f.write(pdf_data)
            n+=1
            print(f"PASS {case_name}: {len(pdf_data)} bytes")
        except Exception as exc:
            failures.append((case_name, str(exc)))
            print(f"FAIL {case_name}: {exc}")

    if failures:
        print("\nFailures:")
        for case_name, msg in failures:
            print(f"- {case_name}: {msg}")
        return 1

    print("\nAll PDF smoke tests passed.")
    return 0


if __name__ == "__main__":
    sys.exit(run())
